from math import factorial


def compute_binom(n, k):
    return int((factorial(n)) / ((factorial(k)) * (factorial(n - k))))


# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
n = int(input())
k = int(input())

# –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é
print(compute_binom(n, k))
